// data types of person
interface person {
   age : number,
    name : string,
    nationality : string ,
    Student : boolean
}
// person object
let person : person = {
    age : 32 ,
    name : 'Sumaira',
    nationality : 'Pakistan',
    Student : true
}
// print person
console.log(person);

